    /**
     * 类型名称
     */
    @TableField(value = "name")
    private String name;

    /**
     * 逻辑删除   1已删除  0未删除   默认为0
     */
    @TableField(value = "is_deleted")
    @TableLogic
    private Byte isDeleted;

